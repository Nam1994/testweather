class Contact:
    def __init__(self, first_name, last_name, email, phone, address, city, state, zipcode, website_domain, hosting,
                 description):
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.phone = phone
        self.address = address
        self.city = city
        self.state = state
        self.zipcode = zipcode
        self.website_domain = website_domain
        self.hosting = hosting
        self.description = description
